print("----------- 其他数据类型转换成字典 -----------")
a=[
    ("name","张三"),
    ("age",28),
    ("gender","男")
]
# 取出姓名 张三
print(a[0][1])
# 把数据转换成字典
b=dict(a)
print(b,type(b))

print("----------- 其他数据类型转换成列表 -----------")
c=(10,20,30)
d=list(c)
print(d,type(d))

print("----------- 其他数据类型转换成集合 -----------")
e=[10, 20, 30,40,10]
f=set(e)
print(f,type(f))


print("----------- 其他数据类型转换成元祖 -----------")
g=[10, 20, 30,40,10]
h=tuple(g)
print(h,type(h))