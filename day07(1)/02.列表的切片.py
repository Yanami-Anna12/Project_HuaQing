"""
    列表的切片和字符串的切片一样
    语法 => 列表[start:end:step]
"""
list1=['a','b','c','d','e']
# 下标  0    1   2   3   4
print(list1[1:]) # 从列表的下标为1处开始截取到末尾
print(list1[3:]) # 从列表的下标为3处开始截取到末尾
print(list1[1:3]) # 从列表的下标为1处开始截取，到下标为3处结束，不包含下标3的元素
print(list1[-4:-1]) # 从列表的下标为-4处开始截取，到下标为-1处结束，不包含下标-1的元素
print(list1[:]) # 截取全部元素
print(list1[::-1]) # 从后向前截取全部元素
print(list1[::2]) # 步长
print(list1[-1:-4]) 